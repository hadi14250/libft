#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	unsigned long	i;
	int				d;

	i = 0;
	d = 0;
	while (src[d] != '\0')
		d++;
	if (!dstsize || dstsize == 0)
		return (d);
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (d);
}
